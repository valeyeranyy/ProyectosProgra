using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace PracticaConstructores
{
    class Triangulo
    {
        private float baseTriangulo = 4;
        private float alturaTriangulo = 3;
    }
    public Triangulo(string valorbaseTriangulo, string valoralturaTriangulo)
    {
        baseTriangulo = valorbaseTriangulo;
        alturaTriangulo = valoralturaTriangulo;

    }
    public void calcAreaTriangulo();
    {
        return = baseTriangulo*alturaTriangulo;
    }
}



